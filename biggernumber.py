# biggernbumber.py -  tells which number is bigger in two inputs

i = float(input("Enter First Number:"))
j = float(input("Enter Second Number:"))
if i > j:
    print(f"{i}is bigger than {j}")
elif j > i:
    print(f"{j} is bigger than {i}")
else:
    print(f"both {i} and {j} are equal")



                